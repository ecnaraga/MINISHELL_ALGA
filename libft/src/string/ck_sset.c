int	ck_sset(char *s, char *set)
{
	int	i;
	int	j;

	i = -1;
	while (s && s[++i])
	{
		j = -1;
		while (set[++j])
		{
			if (s[i] == set[j])
				return (j);
		}
	}
	return (-1);
}
